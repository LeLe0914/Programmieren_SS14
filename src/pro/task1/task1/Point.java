package task1;
/**
 * The class is representation a Point
 * @author Le Wang
 *
 */
public class Point {

    /**
     * The constructor with parameter x-axis and y-axis
     * @param x x-axis
     * @param y y-axis
     */
    public Point(double x, double y) {
       this.x = x;
       this.y = y;
    }
    
    /**
     *  two dimensional Vector, x-axis and y-axis
     */
    double x = 0.0;
    double y = 0.0;
}
